package com.infogain.edoc.model;

public class CurrentEmploymentDetail {
private int applicationId;
private String currentProjectName;
private String teamSize;
private String skillsUsed;
private String rolesAndResponsibilities;
private String organisationDescription;
public int getApplicationId() {
	return applicationId;
}
public void setApplicationId(int applicationId) {
	this.applicationId = applicationId;
}
public String getCurrentProjectName() {
	return currentProjectName;
}
public void setCurrentProjectName(String currentProjectName) {
	this.currentProjectName = currentProjectName;
}
public String getTeamSize() {
	return teamSize;
}
public void setTeamSize(String teamSize) {
	this.teamSize = teamSize;
}
public String getSkillsUsed() {
	return skillsUsed;
}
public void setSkillsUsed(String skillsUsed) {
	this.skillsUsed = skillsUsed;
}
public String getRolesAndResponsibilities() {
	return rolesAndResponsibilities;
}
public void setRoleAndResponsibilities(String rolesAndResponsibilities) {
	this.rolesAndResponsibilities = rolesAndResponsibilities;
}
public String getOrganisationDescription() {
	return organisationDescription;
}
public void setOrganisationDescription(String organisationDescription) {
	this.organisationDescription = organisationDescription;
}


}
